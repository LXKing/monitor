package com.rayton.gps.domain.drivingrecorder;

import java.util.Date;

public class ParameterChangeLog {
    private String number;
    private Date time;
    private short event;

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }

    public short getEvent() {
        return event;
    }

    public void setEvent(short event) {
        this.event = event;
    }
}
